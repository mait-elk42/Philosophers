#include <philo_bonus.h>

int	nsx_valid_args(int ac, char **av)
{
	if (ac != 5 && ac != 6)
		return (-1);
	while (ac-- > 1)
	{
		if (nsx_atoi(av[ac]) <= 0)
			return (-1);
	}
	return (0);
}
